<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				huodongbiaoti: '',
				huodongleixing: '',
				fengmian: '',
				kaishishijian: '',
				huodongdizhi: '',
				jubanfang: '',
				zhiyuanzhanghao: '',
				zhiyuanzhe: '',
				zhiyuanzheshouji: '',
				},
				// 登录用户信息
				user: {},
				ro:{
				   huodongbiaoti : false,
				   huodongleixing : false,
				   fengmian : false,
				   kaishishijian : false,
				   huodongdizhi : false,
				   jubanfang : false,
				   zhiyuanzhanghao : false,
